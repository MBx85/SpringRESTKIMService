package application.department;

public class Department {
	private String deptid;
	private String skz;

	public void setDeptID(String deptid) {
		this.deptid = deptid;
	}
	
	public String getDeptID() {
		return deptid;
	}
	
	public void setSkz(String skz) {
		this.skz = skz;
	}
	
	public String getSkz() {
		return skz;
	}
}
