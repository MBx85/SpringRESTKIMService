package application.kim2dept;

public class KimToDept {
	private String kim;
	private String deptid;

	public void setKim(String value) {
		this.kim = value;
	}

	public String getKIM() {
		return this.kim;
	}

	public void setDeptid(String value) {
		this.deptid = value;
	}

	public String getDeptid() {
		return this.deptid;
	}
}
